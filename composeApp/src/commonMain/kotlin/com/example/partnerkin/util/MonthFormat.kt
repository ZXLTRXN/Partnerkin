package com.example.partnerkin.util


enum class MonthFormat {
    /**
     * Падеж в контексте даты, буквы строчные
     */
    FULL_RUSSIAN,

    /**
     * Именительный падеж, первая буква заглавная
     */
    FULL_RUSSIAN_SIMPLE,

    /**
     * 3 буквенное обозначение месяца, все заглавные
     */
    SHORT_ENG,
}